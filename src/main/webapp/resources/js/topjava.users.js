function enableUser(id) {
    $.ajax({
        type: "POST",
        url: context.ajaxUrl + id,
        data: "enabled=" + $(":checkbox#" + id).prop("checked"),
        success: successNoty("Change success"),
        error: failNoty("Change failed")
    })
}